/** Initialize language from URL parameter */
function initLang() {
  const params = new URLSearchParams(window.location.search);
  // A downloaded report bakes its language in via window.__EMBEDDED_LANG__.
  const lang = params.get("lang") || window.__EMBEDDED_LANG__;
  if (lang && SUPPORTED.includes(lang)) {
    currentLang = lang;
  }
  document.documentElement.lang = currentLang;
}

/** Get current language */
export function getLang() {
  return currentLang;
}

/** Get swisstopo API language code (de, fr, it, en) */
export function getApiLang() {
  return currentLang;
}

/** Translate a key, with optional {placeholder} substitution */
export function t(key, params) {
  const dict = translations[currentLang] || translations[DEFAULT_LANG];
  let text = dict[key] ?? translations[DEFAULT_LANG][key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), v);
    }
  }
  return text;
}

/** Apply translations to all DOM elements with data-i18n attribute */
export function applyI18nDOM() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAria));
  });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  // Update page title
  document.title = t("app.title");
}

/** Change language and reload with ?lang= parameter */
export function setLang(lang) {
  if (!SUPPORTED.includes(lang)) return;
  const url = new URL(window.location);
  url.searchParams.set("lang", lang);
  window.location.href = url.toString();
}

/** Get supported languages for building the dropdown */
export function getSupportedLangs() {
  return SUPPORTED.map((code) => ({
    code,
    label: { de: "DE", fr: "FR", it: "IT", en: "EN" }[code],
  }));
}

/** Number formatter that respects current language */
export function localeFmtNum(n, decimals = 1) {
  const v = parseFloat(n);
  const locale = { de: "de-CH", fr: "fr-CH", it: "it-CH", en: "en-CH" }[currentLang] || "de-CH";
  return isNaN(v) ? "\u2013" : v.toLocaleString(locale, { maximumFractionDigits: decimals });
}

/** Date locale string */
export function getLocale() {
  return { de: "de-CH", fr: "fr-CH", it: "it-CH", en: "en-CH" }[currentLang] || "de-CH";
}

